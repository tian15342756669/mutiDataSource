public class OlpBlockInfoVo {
    private String id;
    private String blockId;
    private String blockName;
    private String zfbAppId;
    private String zfbPartnerId;
    private String zfbDomainUrl;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getBlockId() {
        return blockId;
    }

    public void setBlockId(String blockId) {
        this.blockId = blockId;
    }

    public String getBlockName() {
        return blockName;
    }

    public void setBlockName(String blockName) {
        this.blockName = blockName;
    }

    public String getZfbAppId() {
        return zfbAppId;
    }

    public void setZfbAppId(String zfbAppId) {
        this.zfbAppId = zfbAppId;
    }

    public String getZfbPartnerId() {
        return zfbPartnerId;
    }

    public void setZfbPartnerId(String zfbPartnerId) {
        this.zfbPartnerId = zfbPartnerId;
    }

    public String getZfbDomainUrl() {
        return zfbDomainUrl;
    }

    public void setZfbDomainUrl(String zfbDomainUrl) {
        this.zfbDomainUrl = zfbDomainUrl;
    }
}
